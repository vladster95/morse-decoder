const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    let str = "";
    for (let i = 0,letter = ''; i < expr.length; i++) {
        if (expr[i] === "1") {
            i++;
            expr[i] === "1" ? letter +=  '-' : letter += '.';
            !((i + 1) % 10) ? (str += MORSE_TABLE[letter], letter = '') : null;
        }
        expr[i] === "*" ? (str += ' ' , i+=9) : null;
    }
    return str;
}

module.exports = {
    decode
}